// PDF dimensions (A4)
const PAGE_W = 210;
const PAGE_H = 297;
const MARGIN = 16;
const CONTENT_W = PAGE_W - MARGIN * 2;

function addPageHeader(pdf: jsPDF, pageNum: number, totalPages?: number) {
  // Top bar
  pdf.setFillColor(26, 58, 107);
  pdf.rect(0, 0, PAGE_W, 10, 'F');

  pdf.setFont('helvetica', 'bold');
  pdf.setFontSize(8);
  pdf.setTextColor(255, 255, 255);
  pdf.text('SurveyAI', MARGIN, 6.5);

  pdf.setFont('helvetica', 'normal');
  pdf.setFontSize(7);
  pdf.text('Taurgo × Cardiff University · AI Hackathon 2026', PAGE_W / 2, 6.5, { align: 'center' });

  if (totalPages) {
    pdf.text(`Page ${pageNum} of ${totalPages}`, PAGE_W - MARGIN, 6.5, { align: 'right' });
  }
}

function addPageFooter(pdf: jsPDF) {
  pdf.setDrawColor(229, 231, 235);
  pdf.setLineWidth(0.3);
  pdf.line(MARGIN, PAGE_H - 10, PAGE_W - MARGIN, PAGE_H - 10);

  pdf.setFont('helvetica', 'normal');
  pdf.setFontSize(6.5);
  pdf.setTextColor(156, 163, 175);
  pdf.text(
    'Generated by SurveyAI · Powered by Claude AI (Anthropic) · For informational purposes only. Always consult a qualified RICS Chartered Surveyor.',
    PAGE_W / 2,
    PAGE_H - 6,
    { align: 'center', maxWidth: CONTENT_W }
  );
}

function drawSectionLabel(pdf: jsPDF, label: string, y: number): number {
  pdf.setFont('helvetica', 'bold');
  pdf.setFontSize(7);
  pdf.setTextColor(26, 58, 107);
  pdf.text(label.toUpperCase(), MARGIN, y);

  pdf.setDrawColor(26, 58, 107);
  pdf.setLineWidth(0.3);
  pdf.line(MARGIN, y + 1, PAGE_W - MARGIN, y + 1);

  return y + 5;
}

function drawBox(pdf: jsPDF, x: number, y: number, w: number, h: number, fillHex?: string) {
  if (fillHex) {
    const r = parseInt(fillHex.slice(1, 3), 16);
    const g = parseInt(fillHex.slice(3, 5), 16);
    const b = parseInt(fillHex.slice(5, 7), 16);
    pdf.setFillColor(r, g, b);
    pdf.rect(x, y, w, h, 'F');
  }
  pdf.setDrawColor(229, 231, 235);
  pdf.setLineWidth(0.3);
  pdf.rect(x, y, w, h, 'S');
}

function wrapText(pdf: jsPDF, text: string, x: number, y: number, maxWidth: number, lineHeight: number): number {
  const lines = pdf.splitTextToSize(text, maxWidth);
  pdf.text(lines, x, y);
  return y + lines.length * lineHeight;
}

