import jsPDF from 'jspdf';
import html2canvas from 'html2canvas';

interface DefectCategory {
  icon: string;
  name: string;
  taxonomy_short?: string;
  taxonomy_long?: string;
  confidence: number;
  severity: string;
}

interface Recommendation {
  priority: string;
  action: string;
  specialist: string;
  timeframe: string;
}

interface Citation {
  reference: string;
  title: string;
  relevance: string;
}

interface CostEstimate {
  low: number;
  mid: number;
  high: number;
}

interface Report {
  ref: string;
  date: string;
  previewUrl: string;
  severity: string;
  severityScore: number;
  urgency: string;
  defectCategories: DefectCategory[];
  surveyDescription: string;
  riskMatrix: { likelihood: string; impact: string };
  costEstimate: CostEstimate;
  recommendations: Recommendation[];
  citations: Citation[];
  analysisLimitations?: string;
}

interface SurveyContext {
  propertyType?: string;
  buildingAge?: string;
  locationType?: string;
  reportPurpose?: string;
}

